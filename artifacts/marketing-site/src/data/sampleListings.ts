export function getListingBySlug(slug: string): SampleListing | undefined {
  return sampleListings.find((l) => l.slug === slug);
}

export function getFeaturedListings(): SampleListing[] {
  return sampleListings.filter((l) => l.featured);
}

export function searchListings(query: string): SampleListing[] {
  const q = query.toLowerCase().trim();
  if (!q) return sampleListings;
  return sampleListings.filter(
    (l) =>
      l.address.toLowerCase().includes(q) ||
      l.city.toLowerCase().includes(q) ||
      l.zip.includes(q) ||
      l.slug.includes(q),
  );
}

export function formatPrice(price: number): string {
  return new Intl.NumberFormat("en-US", { style: "currency", currency: "USD", maximumFractionDigits: 0 }).format(price);
}
